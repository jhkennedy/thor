%           doi:10.3189/2013JoG12J159   
%
%   Taylor
%       A simulation of an idealized taylor-like glacier. This simulation was
%       used to generate the Taylor Dome results presented in:
%           Kennedy, J. H., Pettit, E. C., & Di Prinzio, C. L. (2013). 
%           The evolution of crystal fabric in ice sheets and its link to
%           climate history. Journal of Glaciology, 59(214), 357–373.
%           doi:10.3189/2013JoG12J159   
